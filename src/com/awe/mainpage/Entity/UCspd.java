package com.sample;


public class UCspd {

  private long mspdid;
  private String mspdn;
  private String mspdt;


  public long getMspdid() {
    return mspdid;
  }

  public void setMspdid(long mspdid) {
    this.mspdid = mspdid;
  }


  public String getMspdn() {
    return mspdn;
  }

  public void setMspdn(String mspdn) {
    this.mspdn = mspdn;
  }


  public String getMspdt() {
    return mspdt;
  }

  public void setMspdt(String mspdt) {
    this.mspdt = mspdt;
  }

}
